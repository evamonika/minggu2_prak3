  <h2 align=center>PROGRAM STUDI TEKNIK INFORMATIKA</h2>

  <p>
    Pada tahun 2010 berdasar kebutuhan masyarakat dan industri terkait Program Diploma IV 
    bidang informatika maka Polinema mendirikan program studi baru Diploma IV (DIV) Teknik Informatika (TI). 
    Pada awal berdirinya jumlah peserta didik Program D-IV TI hanya terdiri dari 46 Mahasiswa, namun pada tahun 2015 
    jumlah peserta didik Prodi D-IV TI telah mengalami peningkatan menjadi 545 Mahasiswa. Perkembangan jumlah 
    mahasiswa yang sangat pesat baik di Program Studi D-III MI maupun D-IV TI mendorong pimpinan di Polinema untuk 
    menyatukan kedua program studi tersebut dalam satu organisasi jurusan baru terpisah dari jurusan Teknik Elektro. 
    dibawa Jurusan baru terpisah dari Jurusan Teknik Elektro. Dan pada tahun 2015 berdasarkan SK Direktur Nomor 53 dalam rangka peningkatan 
    mutu pengelolaan dan optimasi sumber daya dibentuklah Jurusan Teknologi Informasi (JTI) dengan prodi D-III MI dan D-IV TI.<br><br><br>
  </p>
