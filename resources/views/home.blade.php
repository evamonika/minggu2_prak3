        Kemendiknas No. 045/U/tahun 2002. Untuk mewujudkan tuntutan kebutuhan tersebut, Jurusan Teknologi Informasi
        telah melaksanakan kerjasama dengan berbagai pihak, baik dalam maupun luar negeri sebagai perwujudan
        visi, misi, dan tujuan.<br><br>

        <b>Visi Jurusan Teknologi Informasi Polinema</b>
        Jurusan Teknologi Informasi Polinema sebagai pusat unggulan <b>di bidang teknologi informasi</b>
        dan <b>rekayasa perangkat lunak</b> di tingkat nasional maupun internasional.<br><br><br>
